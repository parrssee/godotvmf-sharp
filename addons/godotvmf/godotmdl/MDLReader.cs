    public class MDLTexture
    {
        public long Address;
        public int NameOffset, Flags, Used, Unused, Material, ClientMaterial;
        public int[] Unused2 = new int[10];
        public string Name = "";

        public static MDLTexture Read(FileAccess file)
        {
            var r = new MDLTexture { Address = (long)file.GetPosition() };
            r.NameOffset = ByteReader.ReadSignedInt(file);
            r.Flags = ByteReader.ReadSignedInt(file);
            r.Used = ByteReader.ReadSignedInt(file);
            r.Unused = ByteReader.ReadSignedInt(file);
            r.Material = ByteReader.ReadSignedInt(file);
            r.ClientMaterial = ByteReader.ReadSignedInt(file);
            for (int i = 0; i < 10; i++) r.Unused2[i] = ByteReader.ReadSignedInt(file);
            return r;
        }
    }

    public class MDLBone
    {
        public long Address;
        public int Id;
        public int NameOffset, Parent;
        public int[] Bonecontrollers = new int[6];
        public Vector3 Pos;
        public Quaternion Quat;
        public Vector3 Rot, PosScale, RotScale;
        public Transform3D PosToBone;
        public Quaternion QAlignment;
        public int Flags, ProcType, ProcIndex, PhysicsBone, SurfacePropIndex, Contents;
        public string Name = "";
        public string SurfaceProp = "";

        public static MDLBone Read(FileAccess file)
        {
            var r = new MDLBone { Address = (long)file.GetPosition() };
            r.NameOffset = ByteReader.ReadSignedInt(file);
            r.Parent = ByteReader.ReadSignedInt(file);
            for (int i = 0; i < 6; i++) r.Bonecontrollers[i] = ByteReader.ReadSignedInt(file);
            r.Pos = ByteReader.ReadVector3(file);
            r.Quat = ByteReader.ReadQuaternion(file);
            r.Rot = ByteReader.ReadEulerVector(file);
            r.PosScale = ByteReader.ReadVector3(file);
            r.RotScale = ByteReader.ReadVector3(file);
            r.PosToBone = ByteReader.ReadTransform3D(file);
            r.QAlignment = ByteReader.ReadQuaternion(file);
            r.Flags = ByteReader.ReadSignedInt(file);
            r.ProcType = ByteReader.ReadSignedInt(file);
            r.ProcIndex = ByteReader.ReadSignedInt(file);
            r.PhysicsBone = ByteReader.ReadSignedInt(file);
            r.SurfacePropIndex = ByteReader.ReadSignedInt(file);
            r.Contents = ByteReader.ReadSignedInt(file);
            for (int i = 0; i < 32; i++) file.Get8(); // unused bytes
            return r;
        }
    }

