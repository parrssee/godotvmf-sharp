    public MDLHeader? Header;
    public List<string> TextureDirs = new();
    public List<MDLTexture> Textures = new();
    public List<MDLBone> Bones = new();
    public List<MDLBodyPart> BodyParts = new();
    public List<List<int>> SkinFamilies = new();
    public string SourcePath = "";

    public string ModelName => System.IO.Path.GetFileNameWithoutExtension(SourcePath);

    private FileAccess? _file;

    public MDLReader(string sourcePath)
    {
        SourcePath = sourcePath;
        _file = FileAccess.Open(sourcePath, FileAccess.ModeFlags.Read);
        if (_file == null) return;

        Header = MDLHeader.Read(_file);
        ReadTextureData();
        ReadBones();
        ReadBodyParts();
        ReadSkinFamilies();

        _file.Close();
        _file = null;
    }

    private void ReadTextureData()
    {
        if (_file == null || Header == null) return;
        _file.Seek((ulong)Header.TexturedirOffset);
        _file.Seek(_file.Get32());
        for (int i = 0; i < Header.TexturedirCount; i++)
            TextureDirs.Add(ByteReader.ReadString(_file));

        Textures = ByteReader.ReadArray(_file, Header.Address, Header.TextureOffset, Header.TextureCount, MDLTexture.Read);
        foreach (var tex in Textures)
            tex.Name = ByteReader.ReadString(_file, tex.Address + tex.NameOffset);
    }

    private void ReadBones()
    {
        if (_file == null || Header == null) return;
        Bones = ByteReader.ReadArray(_file, Header.Address, Header.BoneOffset, Header.BoneCount, MDLBone.Read);
        for (int i = 0; i < Bones.Count; i++)
        {
            var bone = Bones[i];
            bone.Name = ByteReader.ReadString(_file, bone.Address + bone.NameOffset);
            bone.SurfaceProp = ByteReader.ReadString(_file, bone.Address + bone.SurfacePropIndex);
            bone.Id = i;
        }
    }

    private void ReadBodyParts()
    {
        if (_file == null || Header == null) return;
        BodyParts = ByteReader.ReadArray(_file, Header.Address, Header.BodypartOffset, Header.BodypartCount, MDLBodyPart.Read);
        foreach (var bp in BodyParts)
        {
            bp.Name = ByteReader.ReadString(_file, bp.Address + bp.NameIndex);
            bp.Models = ByteReader.ReadArray(_file, bp.Address, bp.ModelIndex, bp.NumModels, MDLModel.Read);
            foreach (var model in bp.Models)
                model.Meshes = ByteReader.ReadArray(_file, model.Address, model.MeshOffset, model.NumMeshes, MDLMesh.Read);
        }
    }

    private void ReadSkinFamilies()
    {
        if (_file == null || Header == null) return;
        _file.Seek((ulong)Header.SkinOffset);
        for (int i = 0; i < Header.SkinfamilyCount * Header.SkinCount; i++)
        {
            int j = i / Header.SkinCount;
            if (SkinFamilies.Count <= j) SkinFamilies.Add(new List<int>());
            SkinFamilies[j].Add(ByteReader.ReadSignedShort(_file));
        }
    }

    public List<string> GetPossibleMaterialPaths()
    {
        var materials = new List<string>();
        foreach (var tex in Textures)
            foreach (var dir in TextureDirs)
                materials.Add(VMFUtils.NormalizePath(dir + "/" + tex.Name));
        return materials;
    }
